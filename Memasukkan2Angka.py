angka1 = float(input("angka pertama: "))
angka2 = float(input("angka kedua: "))

def new_func(angka1, angka2):
    print("penjumlahan: ", int(angka1 + angka2))
    print("pengurangan: ", int(angka1 - angka2))
    print("perkalian: ", int(angka1 * angka2))
    print("pembagian: ", angka1 / angka2)

new_func(angka1, angka2)
